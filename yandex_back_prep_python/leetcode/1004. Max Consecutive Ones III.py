class Solution:
    def longestOnes(self, nums: List[int], k: int) -> int:
        n = len(nums)
        pref_sum = [0] * (n + 1)
        max_len = float("-inf")

        print("n", n)

        if k == n:
            return n

        for i in range(1, n + 1):
            pref_sum[i] += pref_sum[i - 1] + nums[i - 1]

        l = 0
        for r in range(1, n + 1):
            while l <= r and pref_sum[r] - pref_sum[l - 1] < r - l + 1 - k:
                l += 1

            # if pref_sum[r] - pref_sum[l - 1] == r - l + 1 - k:
            max_len = max(max_len, r - l + 1)

        # print("max_len :", max_len)
        return max_len


